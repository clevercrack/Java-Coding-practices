import java.util.*;

public class Arrays {
    public static void main(String args[]) {
        // Simple declaration of an array
        int marks[] = new int[6];
        marks[0] = 12;
        marks[1] = 15;
        marks[2] = 33;
        marks[3] = 155;
        marks[4] = 126;
        marks[5] = 120;

        for (int i = 0; i < 6; i++) {
            System.out.println(marks[i]);
        }
    }
}